# Write a Python program to separate keys and values from a dictionary using keys() and values() methods.

my_dict = {'a': 1, 'b': 2, 'c': 3}  # Initialize a dictionary with some key-value pairs
keys = my_dict.keys()      # Get all keys from the dictionary
values = my_dict.values()  # Get all values from the dictionary
print("Keys:", keys)        # Print the list of keys
print("Values:", values)    # Print the list of values 