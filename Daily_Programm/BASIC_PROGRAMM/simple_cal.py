# Using Functions

def add(no1,no2):
    return no1+no2  
def sub(no1,no2):
    return no1-no2
def mul(no1,no2):
    return no1*no2
def div(no1,no2):
    return no1/no2
def mod(no1,no2):
    return no1%no2

print("Addition is:",add(12,2))
print("Subtraction is:",sub(12,2))  
print("Multiplication is:",mul(12,2))
print("Division is:",div(12,2))
print("Modulus is:",mod(12,2))
